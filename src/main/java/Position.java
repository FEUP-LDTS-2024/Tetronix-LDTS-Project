public class Position {
    private int x;
    private int y;

    public Position(int x_,int y_){
        this.x = x_;
        this.y = y_;
    }

    public int getX(){
        return this.x;
    }

    public void setX(int x_){
        this.x = x_;
    }

    public int getY(){
        return this.y;
    }

    public void setY(){
        this.y = y;
    }
}